x = float(input("Digite o primeiro número:"))
y = float(input("Digite o segundo número:"))

print(f"Soma: {x+y}")

print(f"Subtração: {x-y}")

print(f"Multiplicação: {x*y}")

print(f"Divisão: {x/y}")

print(f"Divisão parte inteira: {x//y}")

print(f"Resto da divisão: {x%y}")

print(f"Exponenciação: {x**y}")

print(f"Manutenção de sinal: {+(x-10)}")

print(f"Inversão de sinal: {-(x-10)}")