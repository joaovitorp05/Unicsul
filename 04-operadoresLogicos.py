# tabela verdade do AND
print("Tabela verdade ou AND")
print(True and True)
print(True and False)
print(False and True)
print(False and False)
print("----------------------")

print("Tabela verdade ou OR")
print(True or True)
print(True or False)
print(False or True)
print(False or False)
print("--------------------")

# exclusivamente tem que ser um ou outro verdadeiro
print("Tabela verdade ou XOR")
print(True != True)
print(True != False)
print(False != True)
print(False != False)
print("--------------------")

print("Operador de Negação (Unário)")
print(not True)
print(not False)
print("--------------------")

print("Cast com operando bool")
print(bool(0))
print(bool(1))