public class CalculadoraSimples {
    public String adicao(int v1, int v2){
        return "O resultado da adição é: " + (v1+v2);
    }
    public String subtracao(int v1, int v2){
        return "O resultado da subtração é: " + (v1-v2);
    }

    public String divisao(int v1, int v2){
        return "O resultado da divisao é: " + (v1/v2);
    }

    public String multiplicacao(int v1, int v2) {
        return "O resultado da multplicação é: " + (v1 * v2);

    }
}
