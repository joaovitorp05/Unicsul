import math

x = 2
y = 10

print("Valor absoluto:")
print(math.fabs(x))
print("--------------------")
print("Teto")
print(math.ceil(y))
print("--------------------")
print("Piso")
print(math.floor(y))
print("------------------")
print("Raiz quadrada")
print(math.sqrt(x))
print("-------------------")
print("Parte inteira (truncamento)")
print(math.trunc(y))
print("-------------------")
print("Fatorial")
print(math.factorial(x))
print("-------------------")
print("Logaritmo")
print(math.log(x, y))
print("-------------------")
print("PI")
print(math.pi)
print("-------------------")